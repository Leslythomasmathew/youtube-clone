// Search videos locally by term
export const searchMockVideos = (searchTerm) => {
  const term = searchTerm.toLowerCase();
  
  // Try exact category match first
  const categoryKeys = Object.keys(mockVideosByCategory);
  const matchedCategory = categoryKeys.find(key => key.toLowerCase() === term);
  if (matchedCategory) {
    return mockVideosByCategory[matchedCategory];
  }
  
  // Otherwise filter all videos
  return allMockVideos.filter(video => {
    return (
      video.snippet.title.toLowerCase().includes(term) ||
      video.snippet.description.toLowerCase().includes(term) ||
      video.snippet.channelTitle.toLowerCase().includes(term)
    );
  });
};
